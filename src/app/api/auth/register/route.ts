import { NextResponse } from "next/server";

// BUG_FIX_CONTEXT: эндпоинт самостоятельной регистрации отключён намеренно.
// Бизнес-модель: доступ выдаёт куратор после оплаты — публичной саморегистрации
// нет. Прежний обработчик создавал аккаунт по прямому POST БЕЗ фиксации согласия
// на обработку ПДн (в отличие от лид-формы), что является и дырой в логике, и
// риском по 152-ФЗ. UI его не вызывает (/register редиректит на /login).
// Оставлен инертным (410 Gone) вместо удаления — для явности и чтобы старые
// прямые вызовы получали понятный ответ, а не 404.

export async function POST() {
  return NextResponse.json(
    { error: "Регистрация недоступна. Доступ оформляется через менеджера после оплаты." },
    { status: 410 },
  );
}

export async function GET() {
  return NextResponse.json({ error: "Method Not Allowed" }, { status: 405 });
}
