-- Нормализация policy_version у лидов.
-- До унификации версий (A7) в колонку policy_version писалась ДАТА редакции
-- ("11.06.2026"); после унификации — единый идентификатор версии "1.0" (та же самая
-- редакция документов). Приводим исторические строки к новому формату, чтобы в одной
-- колонке не сосуществовали две несовместимые кодировки (дата vs версия). Значение
-- эквивалентно: 11.06.2026 — это и есть версия 1.0, поэтому зафиксированное согласие
-- по смыслу не меняется.
UPDATE "leads" SET "policy_version" = '1.0' WHERE "policy_version" = '11.06.2026';
